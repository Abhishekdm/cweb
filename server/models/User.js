const mongoose = require("mongoose");

// creating a Schema for user registeration
const UserSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true,
    unique: true
  },
  password: {
    type: String,
    required: true
  },
  avatar: {
    type: String
  },
  date: {
    type: Date,
    default: Date.now
  }
});
// @desc UserSchema will be mapped as user
module.exports = User = mongoose.model("user", UserSchema);
